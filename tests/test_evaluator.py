# --- evaluate_generated_files ---

def test_evaluate_generated_files_returns_results_for_all():
    from qa_framework_generator.evaluator import evaluate_generated_files
    mock_metric = MagicMock()
    mock_metric.score = 0.9
    mock_metric.reason = "Good"

    files = [_make_page_file("HomePage"), _make_test_file("SmokeTest")]
    config_data = {
        "project_name": "test",
        "package_name": "com.example",
        "pages": [_PAGE_SPEC],
        "flows": [_FLOW_SPEC],
        "environments": {},
        "browsers": ["chrome"],
        "headless_default": True,
        "parallel": {"enabled": False, "thread_count": 1},
        "output_dir": "out",
    }

    with patch("qa_framework_generator.evaluator.GEval", return_value=mock_metric):
        results = evaluate_generated_files(files, config_data, threshold=0.7)

    assert len(results) == 2
    assert all(isinstance(r, ValidationResult) for r in results)


def test_evaluate_skips_non_page_non_test_files():
    from qa_framework_generator.evaluator import evaluate_generated_files
