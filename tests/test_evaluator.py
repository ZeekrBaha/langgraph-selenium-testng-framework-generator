"""Tests for the DeepEval evaluation node — LLM calls are mocked."""
import pytest
from unittest.mock import MagicMock, patch
from qa_framework_generator.state import GeneratedFile, ValidationResult


def _make_page_file(class_name: str = "HomePage", content: str = None) -> GeneratedFile:
    content = content or f"""// Generated by qa-framework-generator
package com.example.pages;

import com.example.base.BasePage;
import org.openqa.selenium.By;
import org.openqa.selenium.WebDriver;

public class {class_name} extends BasePage {{
    private static final By SEARCH_INPUT = By.cssSelector("input[type='search']");

    public {class_name}(WebDriver driver) {{ super(driver); }}

    public void clickSearchInput() {{ click(SEARCH_INPUT); }}
}}
"""
    return GeneratedFile(
        path=f"src/test/java/com/example/pages/{class_name}.java",
        content=content,
        kind="java",
    )


def _make_test_file(class_name: str = "SmokeTest", content: str = None) -> GeneratedFile:
    content = content or f"""// Generated by qa-framework-generator
package com.example.tests;

import com.example.base.BaseTest;
import org.testng.Assert;
import org.testng.annotations.Test;

public class {class_name} extends BaseTest {{
    @Test(groups = {{"smoke"}}, retryAnalyzer = com.example.retry.RetryAnalyzer.class)
    public void {class_name[0].lower() + class_name[1:]}Test() {{
        Assert.assertTrue(driver.getTitle().length() > 0, "Title should not be empty");
    }}
}}
"""
    return GeneratedFile(
        path=f"src/test/java/com/example/tests/{class_name}.java",
        content=content,
        kind="java",
    )


_PAGE_SPEC = {"name": "HomePage", "path": "/", "elements": [{"name": "searchInput", "by": "css", "value": "input[type='search']"}]}
_FLOW_SPEC = {"name": "SmokeTest", "groups": ["smoke"], "steps": [{"open": "HomePage"}]}


# --- evaluate_page_object ---

def test_evaluate_page_object_passes_high_score():
    from qa_framework_generator.evaluator import evaluate_page_object
    mock_metric = MagicMock()
    mock_metric.score = 0.9
    mock_metric.reason = "All elements present and correct"

    with patch("qa_framework_generator.evaluator.GEval", return_value=mock_metric):
        result = evaluate_page_object(_make_page_file(), _PAGE_SPEC, threshold=0.7)

    assert result.passed is True
    assert "0.90" in result.output


def test_evaluate_page_object_fails_low_score():
    from qa_framework_generator.evaluator import evaluate_page_object
    mock_metric = MagicMock()
    mock_metric.score = 0.4
    mock_metric.reason = "Missing locators"

    with patch("qa_framework_generator.evaluator.GEval", return_value=mock_metric):
        result = evaluate_page_object(_make_page_file(), _PAGE_SPEC, threshold=0.7)

    assert result.passed is False
    assert "eval_page_object" in result.name


def test_evaluate_test_class_passes_high_score():
    from qa_framework_generator.evaluator import evaluate_test_class
